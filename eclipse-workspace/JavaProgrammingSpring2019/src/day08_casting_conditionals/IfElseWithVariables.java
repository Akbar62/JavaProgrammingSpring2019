package day08_casting_conditionals;

public class IfElseWithVariables {

	public static void main(String[] args) {
		int temperature = 45;
		
		if(temperature >= 65) {
			System.out.println("It is a nice day! Lets code java");
		}else {
			System.out.println("Stay home and code java");
		}
		
		int teamAScore = 121;
		int teamBScore = 102;
		
		if(teamAScore > teamBScore) {
			System.out.println("Team A won\nGo team A");
		}else {
			System.out.println("Team B won \nGo teams!");
		}
		
		
	}

}
