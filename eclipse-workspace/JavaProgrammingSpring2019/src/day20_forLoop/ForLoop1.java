package day20_forLoop;

public class ForLoop1 {

	public static void main(String[] args) throws InterruptedException {
		
		for(int i = 0; i <= 20; i++) {
			System.out.println("I love java!");
		}
		
		for(int j = 5; j <= 15; j++) {
			System.out.println(j);
		}
		
		for(int ak = 20; ak >= 10; ak--) {
			System.out.print(ak+" ");
			Thread.sleep(456);
		}

	}

}
